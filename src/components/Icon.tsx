export const ICON_NAMES: string[] = Object.keys(ICON_DEFS);

/** Public alias for the icon name → inner-SVG map. */
export const IconMap = ICON_DEFS;

export interface IconProps {
  /** Icon name from {@link ICON_NAMES}. Unknown names render a neutral glyph. */
  name: string;
  /** Pixel size (width === height). Defaults to 24. */
  size?: number | string;
  strokeWidth?: number;
  className?: string;
  /** Accessible label. When omitted the icon is treated as decorative. */
  title?: string;
}

const FALLBACK = ICON_DEFS["check-circle"];

/**
 * Render an icon by name from the shared {@link ICON_DEFS} set. Inherits color
 * from `currentColor`, so set the parent's text color to recolor it.
 */
export function Icon({
  name,
  size = 24,
  strokeWidth = 2,
  className,
  title,
}: IconProps) {
  const inner = ICON_DEFS[name] ?? FALLBACK;
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      role={title ? "img" : undefined}
      aria-hidden={title ? undefined : true}
      aria-label={title}
      dangerouslySetInnerHTML={{ __html: inner }}
    />
  );
}
